export function createMockProducts(count: number, overrides: Partial<Product>[] = []): Product[] {
  return Array.from({ length: count }, (_, i) => 
    createMockProduct({
      id: i + 1,
      name: `Product ${i + 1}`,
      ...overrides[i],
    })
  );
}